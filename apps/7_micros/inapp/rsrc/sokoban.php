<?php

$level = [

    "                    ",
    "    #####           ",
    "    #   #           ",
    "    #*  #           ",
    "  ###  *##          ",
    "  #  * * #          ",
    "### # ## #   ###### ",
    "#   # ## #####  ++# ",
    "# *  *          ++# ",
    "##### ### # ##  ++# ",
    "    #     ######### ",
    "    #######         ",

];

$out = [];

for ($i = 0; $i < 12; $i++)
for ($j = 0; $j < 20; $j++) {

    $m = $level[$i][$j];
    $k = $j >> 2;
    $v = $j & 3;

    if ($m == '#') $m = 1;
    else if ($m == '*') $m = 2;
    else if ($m == '+') $m = 3;
    else $m = 0;

    @ $out[$i*5 + $k] |= ($m << (6 - 2*$v));
}

for ($i = 0; $i < 60; $i++) {

    $v = sprintf("0x%02x, ", $out[$i]);
    echo $v;
    if (($i % 5) == 4) echo "\n";
}

